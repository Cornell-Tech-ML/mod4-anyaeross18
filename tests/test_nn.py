@pytest.mark.task4_3
@given(tensors(shape=(1, 1, 4, 4)))
def test_avg(t: Tensor) -> None:
    out = minitorch.avgpool2d(t, (2, 2))
    assert_close(
        out[0, 0, 0, 0], sum([t[0, 0, i, j] for i in range(2) for j in range(2)]) / 4.0
    )

    out = minitorch.avgpool2d(t, (2, 1))
    assert_close(
        out[0, 0, 0, 0], sum([t[0, 0, i, j] for i in range(2) for j in range(1)]) / 2.0
    )

    out = minitorch.avgpool2d(t, (1, 2))
    assert_close(
        out[0, 0, 0, 0], sum([t[0, 0, i, j] for i in range(1) for j in range(2)]) / 2.0
    )
    minitorch.grad_check(lambda t: minitorch.avgpool2d(t, (2, 2)), t)


@pytest.mark.task4_4
@given(tensors(shape=(2, 3, 4)))
def test_max(t: Tensor) -> None:
    out = minitorch.max(t, 1)
    # assert out.shape == t.shape, f"out = {out}, t = {t}"
    for i in range(2):
        for j in range(4):
            assert_close(out[i, 0, j], max([t[i, k, j] for k in range(3)]))

    out = minitorch.argmax(t, 1)
    # assert out.sum(dim=1).all().item() == 1, f"out = {out}, t = {t}, sum_dim = {t.sum()}"
    # assert out.shape == t.shape, f"out = {out}, t = {t}, sum_dim = {out.sum(dim=1).all().item()}"
    for i in range(2):
        for j in range(4):
            max_val = max([t[i, k, j] for k in range(3)])
            for k in range(3):
                if t[i, k, j] == max_val:
                    assert_close(out[i, k, j], 1.0)
                else:
                    assert_close(out[i, k, j], 0.0)
    z = zeros(t.shape)
    minitorch.grad_check_max(
        lambda t: minitorch.max(t, dim=1), t, sum_dim=out.sum(dim=1) + z, arg_max=out
    )


